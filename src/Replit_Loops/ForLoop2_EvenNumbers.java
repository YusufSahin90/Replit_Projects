package Replit_Loops;

public class ForLoop2_EvenNumbers {
    public static void main(String[] args) {
        //Kodu aşağıya yazınız.

        for (int x = 0; x <= 100; x++) {

            if (x % 2 == 0) {

                System.out.println(x);
            }
        }
    }
}

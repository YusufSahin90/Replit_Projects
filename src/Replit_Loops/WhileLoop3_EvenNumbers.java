package Replit_Loops;

public class WhileLoop3_EvenNumbers {
    public static void main(String[] args) {

        int x = 30;

        //Kodu aşağıya yazınız.

        while(x > 0){

            x--;

            if(x % 2 == 0)
                System.out.println(x);
        }

    }
}

package Replit_Loops;

public class NestedLoop1 {
    public static void main(String[] args) {
        //Kodu aşağıya yazınız.

        for (int satir = 0; satir < 7; satir++){

            for (int sutun = 0; sutun < 5; sutun++){

                System.out.print( "#" );
            }
            System.out.println();
        }
    }
}

package Replit_Loops;

public class ForLoop3_OddNumbers {
    public static void main(String[] args) {
        //Kodu aşağıya yazınız.

        for (int x = 0; x <= 100; x++) {

            if (x % 2 == 1) {

                System.out.println(x);
            }
        }
    }
}
